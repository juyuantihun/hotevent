-- 地理坐标表
CREATE TABLE IF NOT EXISTS geographic_coordinates (
    id BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '主键ID',
    location_name VARCHAR(255) NOT NULL COMMENT '地点名称',
    location_type VARCHAR(50) NOT NULL COMMENT '地点类型(COUNTRY/REGION/CITY/DISTRICT)',
    latitude DECIMAL(10, 6) NOT NULL COMMENT '纬度',
    longitude DECIMAL(10, 6) NOT NULL COMMENT '经度',
    country_code VARCHAR(10) COMMENT '国家代码',
    region_code VARCHAR(50) COMMENT '地区代码',
    is_default BOOLEAN DEFAULT FALSE COMMENT '是否为默认坐标',
    accuracy_level VARCHAR(20) DEFAULT 'DEFAULT' COMMENT '精度等级(HIGH/MEDIUM/LOW/DEFAULT)',
    data_source VARCHAR(100) DEFAULT 'SYSTEM' COMMENT '数据来源',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    is_deleted BOOLEAN DEFAULT FALSE COMMENT '是否删除',
    
    -- 索引
    INDEX idx_location_name (location_name),
    INDEX idx_location_type (location_type),
    INDEX idx_coordinates (latitude, longitude),
    INDEX idx_country_code (country_code),
    INDEX idx_is_default (is_default),
    UNIQUE KEY uk_location_name_type (location_name, location_type, is_deleted)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='地理坐标表';

